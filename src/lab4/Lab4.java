    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (isCircular()){
            if (c.isEmpty()){
                JOptionPane.showMessageDialog(this, "No value to dequeue", "Queue Underflow", JOptionPane.ERROR_MESSAGE);
                return;
            }
            Dequeued.setText(String.valueOf(c.dequeue()));
        } else if (isLinear()){
            if (l.isEmpty()){
                JOptionPane.showMessageDialog(this, "No value to dequeue", "Queue Underflow", JOptionPane.ERROR_MESSAGE);
                return;
            }
            Dequeued.setText(String.valueOf(l.dequeue()));
        } else {
            JOptionPane.showMessageDialog(this, "Error");
            return;
        }
        Display();
    }//GEN-LAST:event_jButton3ActionPerformed

