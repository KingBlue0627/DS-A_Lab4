    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String input = Input.getText();
        try {
            Float.parseFloat(input);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Please Input numeric values","Input Error",JOptionPane.ERROR_MESSAGE);
            Input.setText("");
            return;
        }
        try {
            Integer.parseInt(input);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Please Input integer values","Input Error",JOptionPane.ERROR_MESSAGE);
            Input.setText("");
            return;
        }
        if (isCircular()){
            if (c.isFull()){
                JOptionPane.showMessageDialog(this, "Queue capacity reached", "Queue Overflow", JOptionPane.ERROR_MESSAGE);
                return;
            } else {
                c.enqueue(Integer.parseInt(input));
            }
            
        } else if (isLinear()){
            if (l.isFull()){
                JOptionPane.showMessageDialog(this, "Queue capacity reached", "Queue Overflow", JOptionPane.ERROR_MESSAGE);
                return;
            } else {
                l.enqueue(Integer.parseInt(input));
            }
            
        } else {
            JOptionPane.showMessageDialog(this, "Error");
            return;
        }
        Input.setText("");
        Display();
    }//GEN-LAST:event_jButton2ActionPerformed

